'''5.	En un salón de clase nos pide diseñar un algoritmo que permita determinar el porcentaje de 
varones y el porcentaje de mujeres. Cantidad de Niños 78 y Niñas 43.'''

#Algoritmos de porcentajes

#Definiciones
ninos=78
ninas=43

#Cálculos
suma=ninos+ninas
por_ninos=ninos*100/suma
por_ninas=ninas*100/suma

#Impresiones
print("El porcentaje de niños es de: " + str(round(por_ninos)) + "%")
print("El porcentaje de niñas es de: " + str(round(por_ninas)) + "%")